public class loops {
    public static void main(String[] args) {
        //this is a while loop
        Integer x;
        x=0;
        while (x<5){
            System.out.println(x);
            x++;
        }

        //this is a do while loop
        Integer y;
        y=0;
        do{
            System.out.println(y);
            y++;
        }while (y<5);

        //This is a forloop
        for (int a=0; a<5;a++){
        System.out.println(a);
    }
}}
